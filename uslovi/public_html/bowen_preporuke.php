<p>Bowen tretman ne prestaje u trenutku kada se seansa završi. Telo nastavlja da obrađuje primljene poruke i da se, sopstvenim tempom, vraća u ravnotežu.</p> 
<p>Sledeće preporuke služe kao podrška tom procesu i pomažu da se efekti tretmana što bolje integrišu.</p>

<p>1. Prostor za delovanje Bowen tretmana</p>
<p>U narednih 7 dana, nakon Bowen tretmana, preporučuje se da se telo ostavi da samostalno integriše promene, bez dodatnih manuelnih ili energetskih intervencija, kao što su:
<ul>
<li>masaža</li>
<li>kiropraktika</li>
<li>fizioterapija</li>
<li>akupunktura</li>
<li>magnetna terapija</li>
</ul>
</p>
<p>Na ovaj način omogućava se da se balans postignut Bowen tretmanom očuva i produbi.</p>

<p>2. Podrška telu u prvoj nedelji</p>
<p>U prvoj nedelji nakon tretmana, male svakodnevne prilagodbe mogu značajno doprineti osećaju stabilnosti i lakoće u telu:
<ul>
<li>Prilikom ustajanja iz kreveta, sa stolice ili izlaska iz automobila, preporučuje se da ravnomerno prenesete težinu na oba stopala.</li>
<li>Tretirane regije je dobro štititi od preteranog grejanja ili hlađenja.</li>
<li>Tuširanje se preporučuje mlakom vodom, uz izbegavanje direktnog mlaza veoma tople vode preko vrata ili drugih tretiranih zona.</li>
<li>U ovom periodu nije potrebno koristiti topla ili hladna pakovanja.</li>
<li>Na dan tretmana prijaće, ako izbegavate dugotrajno sedenje (duže od 30 minuta u kontinuitetu).</li>
<li>Naporno vežbanje se ne preporučuje, dok su lagane šetnje u naredna tri dana veoma poželjne – najmanje 15 minuta dnevno.</li>
</ul>
</p>

<p>3. Hidratacija i svakodnevne navike

<ul>
<li>Dovoljan unos vode (6–8 čaša dnevno) pomaže telu da lakše eliminiše toksine koje su mišići i vezivno tkivo oslobodili tokom tretmana.</li>
<li>U ovom periodu preporučuje se smanjen unos stimulišućih napitaka poput kafe i gaziranih pića.</li>
<li>Lekovi protiv bolova ili antiinflamatorni lekovi mogu se uzimati po potrebi.</li>
<li>Važno je da nastavite sa terapijom koju vam je propisao vaš lekar.</li>
</ul>
</p>

<p>4. Posebna pažnja – vilični (TM) zglob</p>
<p>Ukoliko je tokom tretmana rađena procedura za temporomandibularni (vilični) zglob, u naredna 24 sata preporučuje se:
<ul>
<li>blaže i svesnije žvakanje</li>
<li>izbegavanje tvrde hrane</li>
<li>izbegavanje preteranog otvaranja usta</li>
</ul>
</p>
<p>Ove mere pomažu da se tretman stabilno integriše.</p>

<p>5. Prirodne reakcije organizma<p/>
<p>Nakon Bowen tretmana moguće su različite reakcije, koje predstavljaju normalan odgovor tela na započeti proces balansa. One mogu uključivati:
<ul>
<li>osećaj toplote ili hladnoće</li>
<li>blago crvenilo lica</li>
<li>emotivno oslobađanje</li>
<li>prolaznu glavobolju</li>
<li>bolove u telu</li>
<li>osećaj nalik blagoj prehladi ili povišenoj temperaturi</li>
<li>telo može proći kroz bilo koji simptom koje je ikada imalo</li>
</ul>
</p>
<p>Kod nekih osoba promene su veoma suptilne i odvijaju se postepeno, pa mogu proći gotovo neprimećeno. I jedno i drugo je u redu. Telo uvek reaguje na način koji mu je u tom trenutku najprirodniji.</p>
<p>Svaki odgovor organizma, pa i onaj najtiši, znak je da je proces samoregulacije pokrenut.</p> 

<p>6. Osetljivost mišića

<ul>
<li>Tokom nekoliko dana nakon prva 1–2 tretmana, može se javiti blaga bolna osetljivost mišića.</li>
<li>Ovaj osećaj se često poredi sa upalom mišića nakon fizičke aktivnosti i smatra se uobičajenom i prolaznom pojavom.</li>
</ul>
</p>

<p>7. Vežbe kao podrška oporavku

<ul>
<li>U zavisnosti od vašeg stanja, možete dobiti jednostavne vežbe za izvođenje kod kuće.</li>
<li>Njihovo pravilno i svesno izvođenje doprinosi većoj pokretljivosti zglobova i dodatno podržava proces oporavka.</li>
</ul>
</p>

<p>Ako imate bilo kakva pitanja ili niste sigurni kako da neku preporuku primenite u svakodnevnom životu, slobodno mi se obratite.</p>
<p>Bowen tretman je proces – nežan, ali dubok – a podrška između tretmana čini da njegovi efekti budu stabilniji i dugotrajniji.</p>


                          
