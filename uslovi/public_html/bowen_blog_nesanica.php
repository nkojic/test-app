<p>Večeras je legao ranije, već oko 11h. Da proba, možda će prevariti mozak koji radi neprekidno. 12h je… ništa… 1h… ništa… ide do kupatilia, pa i do kuhinje da popije čašu vode. Možda se nešto promeni.. Eto, opet je 3h…, a sutra ima važan sastanak..</p>
<p>Juče je uspeo da odspava koliko toliko… vezao je 4h… od ponoći do 4h ujutru i onda, ništa… mora da se pozabavi ovim problemom. Ne razume.. jeste opterećen na poslu, pa i život ga baš ne mazi, ali i pre je bilo tako, pa je spavao….</p>
<p>Dođe tako trenutak u životu kad nam je, jednostavno, previše.. svega… i telo reaguje.. na razne načine. Jedan od načina je da se ne smiruje, ne opušta, čak ni onda kad bi trebalo, kada mu je neophodno. Kroz san se telo i mozak regenerišu. Ta “pauza”je neophodna da bi čovek mogao da funkcioniše na dnevnom nivou. Kvalitet života da ne spominjem. Često se prvo poseže za najlakšim rešenjima, koja često nisu prava, naprotiv. Ljudi piju kafe, kao i razna druga stimulativna pića, energetska pića i sl. U prvom trenutku se osećaju bolje, dobiju energiju, a onda, kasnije, samo da preguraju dan. Posle više ni ne deluje jer su se navikli, ali, često ne prestaju.</p>
<p>Šta god da se dešava, problem je u pozadini. Autonomni nervni sistem se ne smiruje. Nikada, ili vrlo retko i kratko prelazi u svoj relaksirani mod, parasimpatikus. Nema dovoljno vremena, niti dovoljno kavlitetnog odmora. Najčešće se to stanje produži i, onda, se posledice pojave na raznim mestima. Nekada su očite, a nekda i nisu dok ne dođe do tačke bez povratka.</p> 
<p>Prvi efekat Bowen terapije je relaksacija. Ona se dešava na svim nivoima. I na fizičkom, i na mentalnom, i na emotivnom. Bowen pokretima se šalje poruka autonomnom nervnom sistemu da je bezbedno da se opusti. Ne vezano za našu volju, sistem se opušta. Samim tim, polako se balansiraju sve funkcije u telu i pokreće se proces regeneracije. Um dozvoljava telu da se obnovi. Kavlitetan san se vraća. Odmorno telo. Odmoran um. Posledično se povećava i količina energije. Tada dolazi do prave promene. Ljudi postaju zadovoljniji jer, na dnevnom nivou, mogu mnogo više i mnogo lakše. Raspoloženiji su. Otvoreniji za druženja, izlaske, putovanja, sve lepe stvari koje nam život donosi.</p>
                          
