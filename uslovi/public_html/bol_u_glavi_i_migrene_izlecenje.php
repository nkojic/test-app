<p>Glavobolje i migrene nisu samo prolazna neprijatnost. One često predstavljaju način na koji telo signalizira da je preopterećeno — fizički, emocionalno ili mentalno. Mogu se javiti povremeno ili postati hronične, narušavajući koncentraciju, san, raspoloženje i kvalitet svakodnevnog života.</p>
<p>Bol u glavi, često je, poruka tela. Povezan sa napetošću u vratu, ramenima i vilici, stresom, hormonskim promenama, poremećajem sna ili preopterećenjem nervnog sistema. Migrene, osim intenzivnog bola, mogu uključivati mučninu, osetljivost na svetlo i zvuk, kao i osećaj potpune iscrpljenosti.</p>
<p>Bowen terapija je nežna, ali duboko delotvorna manualna metoda koja deluje na uzroke, a ne samo na simptome. Preciznim, laganim pokretima preko mišića, fascija i struktura povezanih sa vratom, ramenima i nervnim sistemom, Bowen terapija pomaže telu da se opusti i reorganizuje.</p>
<p>Ova metoda može biti podrška kod:
<ul>
<li>	tenzionih glavobolja </li>
<li>	migrena sa ili bez aure</li>
<li>	glavobolja povezanih sa stresom</li>
<li>	bolova koji potiču iz vrata i ramena</li>
<li>	učestalih ili hroničnih glavobolja</li>
</ul>
</p>
<p>Za razliku od invazivnih ili agresivnih pristupa, Bowen terapija ne forsira telo, već mu omogućava da izađe iz stanja konstantne napetosti. Mnogi klijenti primećuju smanjenje učestalosti i intenziteta glavobolja, kao i osećaj olakšanja, jasnoće i dubokog smirenja nakon tretmana.</p>
<p>Bowen terapija pomaže nervnom sistemu da se resetuje, čime se smanjuje preosetljivost na spoljašnje i unutrašnje nadražaje. Kada se smanji napetost, bol gubi snagu. Telo ponovo pronalazi ravnotežu, cirkulacija se poboljšava, mišići se opuštaju, a glava dobija prostor da se odmori.</p>
<p>Glavobolje i migrene ne moraju da budu stalni pratilac. One su, često, poziv tela da se uspori i vrati u balans.</p>
<p>Ako se borite sa čestim glavoboljama ili migrenama koje utiču na vaš svakodnevni život, Bowen terapija može biti nežna, ali efikasna podrška Vama i vašem telu.</p>
<p>Zakažite svoj termin i napravite prvi korak ka bezbolnoj i bistrijoj glavi i većem miru.</p>
<p>Kontaktirajte me kako bismo zajedno procenili da li je Bowen terapija pravi izbor za vas.</p>
<p>Olakšanje je moguće — kada telu damo priliku da se oporavi.</p>


                          
