<p>Bowen tretman ne deluje agresivno na telo, već aktivira njegovu prirodnu sposobnost samoregulacije.</p>
<p>Tretman se najčešće izvodi u ležećem položaju, ali se po potrebi radi i sedeći ili stojeći. Tokom rada izvode se serije vrlo nežnih, ali izuzetno preciznih Bowen pokreta. Iako deluju minimalno, njihov efekat je dubok — jer ne deluju na silu, već direktno komuniciraju sa nervnim sistemom.</p>
<p>Pokreti se izvode palčevima ili kažiprstima, na precizno određenim mestima na telu, preko mišića, tetiva, ligamenata i fascija. Mogu se raditi na koži ili preko lagane odeće. Ono što se dešava u telu često se opisuje kao talas ili vibracija. To je odgovor organizma koji prepoznaje poruku i počinje da se opušta tamo gde se dugo zadržavala napetost.</p>
<p>Sam Bowen pokret često se opisuje kao pokret nalik sviranju žica na gitari. Kada se jednom aktiviraju, mišići i tetive odgovaraju blagim talasanjem, od površine ka dubljim slojevima. Ovim se telu daje signal da otpusti napetost koja je nastala kao zaštitni, kompenzatorni mehanizam, često prisutan nakon povrede, stresa ili dugotrajnog opterećenja.</p>
<p>Pokreti se izvode u tačno utvrđenom redosledu i smeru, kako bi se postigao najbolji mogući efekat. Nakon svake serije pokreta, terapeut pravi pauzu od najmanje dva minuta, tokom koje klijent ostaje sam u prostoru. Ove pauze su sastavni i izuzetno važan deo Bowen tehnike — one daju telu vreme da obradi primljene informacije, opusti se i započne proces integracije.</p>
<p>Jedan tretman traje u proseku 45 do 60 minuta. Preporučeni razmak između tretmana je 7 do 10 dana, kako bi organizam imao dovoljno vremena da završi proces koji je započet. Promene se često nastavljaju i danima nakon tretmana — ponekad su suptilne, ponekad vrlo jasne.</p>
<p>Kombinacijom i redosledom Bowen pokreta može se raditi sa celim telom ili vrlo ciljano, u zavisnosti od toga šta je u tom trenutku prioritet. Bowen terapija ne briše simptome — ona resetuje sistem koji ih stvara.</p>
<p>Na taj način telo se podseća svog prirodnog obrasca zdravlja, svog blueprinta. Aktivira se ćelijska memorija i brišu se stari kompenzatorni obrasci, čak i oni koji su nastali godinama unazad.</p>
<p>Ako osećate da vaše telo traži drugačiji pristup — tiši, precizniji i dublji — možda je vreme da mu to i omogućite.</p>

                          
