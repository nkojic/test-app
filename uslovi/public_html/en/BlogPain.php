<p>Pain is good. It is our friend that shows us that something is not right. It reveals where our limits are. It gives us the opportunity to take care of ourselves as soon as possible. 
People who do not have the ability to feel pain do not have an easy life. As a consequence, they often have serious health problems. Their lives are very limited and conditioned by many factors. So all of us who can feel pain are, in a way, blessed.
However, as I said, pain is a signal and it should be acknowledged and respected, but that does not mean we should live with it, become accustomed to it, or accept it as normal.</p>
<p>Although we often receive the message that some things must hurt — they don’t. That pain is related to age — that doesn’t have to be true either. That we must endure it and wait for it to pass. Why would anyone do that in the 21st century?</p>
