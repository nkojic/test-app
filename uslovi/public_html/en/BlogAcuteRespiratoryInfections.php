<p>Who would have thought that something so small and invisible could disrupt our quality of life so much, throw us out of balance, and put us to bed? The well-known viruses and bacteria at work.</p>
<p>For as long as I can remember there have been illnesses with fever, sneezing, coughing, sore throat, and consequently chest pain. Weakness, muscle and joint pain — when people say, “every bone in my body hurts.” And then it puts you in bed and you can’t even lift your head. Sometimes it lasts a day or two, but sometimes it drags on and simply won’t go away.
Since the time of Corona, all of this has risen to a higher level. People became afraid, some with good reason. When fear takes over, immunity weakens even more.</p>
<p>Since I have been practicing Bowen therapy, my children have rarely been ill. They take antibiotics even less often. I am very grateful since Bowen therapy became part of my family. It is the same with my clients. They come with fever, coughing, sneezing, not feeling well.</p>
