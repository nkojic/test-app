<p>During flu season, clients who come for completely different reasons do not get sick. Bowen therapy works in the background on the immune system. The body becomes so balanced and strong that, no matter how great the exposure to viruses, the immune system resolves it in the background. Their quality of life does not fluctuate. They work, socialize, go to restaurants, theatres, cinemas, travel, exercise. For them, life does not stop — on the contrary, they live it to the fullest!</p>

                          
