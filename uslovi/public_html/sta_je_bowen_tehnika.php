<p>Bowen terapijska tehnika je potpuno prirodna, nežna i holistička metoda koja telu daje jasan signal da se samo vrati u ravnotežu.</p>
<p>Zasniva se na jednostavnom, ali dubokom principu:</p>
<p>kada se telo zaista opusti, aktiviraju se njegovi urođeni mehanizmi samoregulacije i regeneracije.</p>

<p>Osnovne karakteristike Bowen tehnike
<ul>
<li>	Bowen je relaksaciona, neinvazivna i veoma efikasna manuelna tehnika </li>
<li>	Deluje na disbalanse na svim nivoima – fizičkom, biohemijskom, psihološkom i emocionalnom</li>
<li>	Ne forsira promenu, već podstiče telo da samo pronađe put ka balansu</li>
<li>	Bezbedna je i nežna, prilagođena individualnom ritmu svake osobe</li>
</ul>
</p>
<p>Osnivač tehnike, Tom Bowen, polazio je od saznanja da se najdublji procesi oporavka aktiviraju upravo u stanju potpune relaksacije.</p>

<p>Kako Bowen deluje na telo?
<ul>

<li>	Terapija se izvodi preko mekih tkiva: mišića, tetiva, ligamenata i fascija</li>
<li>	Bowen pokreti su precizni, lagani i kratki, a izvode se na tačno određenim mestima na telu</li>
<li>	Nakon serija pokreta prave se pauze od nekoliko minuta, tokom kojih telo integriše primljene informacije</li>
</ul>
</p>
<p>Upravo te pauze čine Bowen tehniku jedinstvenom – one daju telu prostor da integriše poruku i odgovori na svoj način.</p>

<p>Šta se dešava tokom tretmana?
<ul>
<li>	Lagani, rolajući pokreti stimulišu nervni sistem</li>
<li>	Nervni impulsi se ne zadržavaju lokalno, već se prenose kroz celo telo</li>
<li>	Aktivira se parasimpatički nervni sistem – stanje odmora, oporavka i obnove</li>
</ul>
</p>
<p>Mnogi klijenti tokom tretmana osećaju:
<ul>
<li>	blago peckanje ili trnjenje</li>
<li>	talase toplote</li>
<li>	osećaj strujanja</li>
<li>	dubok unutrašnji mir</li>
<li>	utisak da se „nešto pokreće“ i nastavlja i nakon što terapeut skloni ruke</li>

</ul>
</p>

<p>Sve su to znaci da telo reaguje i uspostavlja novi balans.</p>

<p>Šire koristi Bowen tehnike</p>
<p>Pored duboke relaksacije:
<ul>
<li>	mišići i fascije se postepeno otpuštaju</li>
<li>	disanje postaje sporije i dublje</li>
<li>	srčani ritam se smiruje</li>
<li>	poboljšava se cirkulacija</li>
<li>	podstiče se eliminacija toksina iz tkiva</li>
</ul>
</p>
<p>Bowen na taj način:
<ul>
<li>	smanjuje mišićnu napetost</li>
<li>	redukuje fizički i emocionalni stres</li>
<li>	pomaže nervnom sistemu da se „resetuje“</li>
<li>	vraća osećaj mira i stabilnosti u celom telu</li>
</ul>
</p>
<p>Komplementarna i bezbedna metoda
<ul>
<li>	Bowen tehnika ne zamenjuje klasičnu medicinu, već je nadopunjuje</li>
<li>	Smatra se komplementarnom metodom i može se primenjivati uz standardne terapije</li>
<li>	
<ul>
<li>Zbog svoje nežnosti i bezbednosti, pogodna je za:</li>
<li>	bebe</li>
<li>	decu</li>
<li>	odrasle</li>
<li>	starije osobe
</ul></li>
</ul>
</p>
<p>Primena Bowen tehnike proširena je i na životinje – konje, pse i mačke – sa izuzetno dobrim rezultatima, što dodatno potvrđuje da se radi o autentičnom fiziološkom odgovoru organizma.</p>

<p>Zašto ljudi biraju Bowen?</p>
<p>Zato što je:
<ul>
<li>	jednostavna, a dubinska </li>
<li>	blaga, a snažna </li>
<li>	nenametljiva, a efikasna</li>
</ul>
</p>
<p>Često iznenadi upravo svojom sposobnošću da pokrene promene tamo gde druge metode nisu uspele.</p>
<p>Ako osećate da je vašem telu potreban prostor da se umiri, resetuje i vrati u ravnotežu, Bowen može biti upravo taj prvi, nežni korak.</p>
<p>Saznajte da li je Bowen terapija za Vas.</p>


                          
